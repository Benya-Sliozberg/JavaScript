// let - Объявление переменной
// prompt(текст_сообщения, значение_по_умолчанию) - функция, запрашивающая информацию у пользователя
let name = prompt("Введите имя", "Вася")

// Вывод информации в консоль
console.log(name)

// Вывод информации на страницу
document.write(`<h1>${name} вошёл на сайт!</h1>`)